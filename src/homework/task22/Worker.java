package homework.task22;

public class Worker {
    String name;
    String city;
    int age;

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public int getAge() {
        return age;
    }

    public Worker(String name, String city, int age) {
        this.name = name;
        this.city = city;
        this.age = age;
    }
}
